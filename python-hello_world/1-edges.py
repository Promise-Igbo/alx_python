# File: 1-edges.py

# Given word
word = "Holberton"

# Extracting the first 3 letters of the word
word_first_3 = word[:3]

# Extracting the last 2 letters of the word
word_last_2 = word[-2:]

# Extracting the middle word (without the first and last letters)
middle_word = word[1:-1]

# Printing the results
print("First 3 letters:", word_first_3)
print("Last 2 letters:", word_last_2)
print("Middle word:", middle_word)
